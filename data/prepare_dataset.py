# Build mapping: original code → new 0-indexed ID
CODE_TO_IDX = {code: idx for idx, code in enumerate(ORIGINAL_CODES)}


def remap_label(src_path: Path, dst_path: Path) -> None:
    """Remap class IDs in a label file."""
    lines = src_path.read_text().strip().splitlines()
    remapped = []
    for line in lines:
        parts = line.strip().split()
        if len(parts) < 5:
            continue
        orig_cls = int(parts[0])
        if orig_cls not in CODE_TO_IDX:
            continue
        new_cls = CODE_TO_IDX[orig_cls]
        remapped.append(f"{new_cls} {' '.join(parts[1:])}")
    dst_path.write_text("\n".join(remapped) + "\n" if remapped else "")


def main() -> None:
    # Clean destination
    if DST.exists():
        shutil.rmtree(DST)

    for split in ("train", "val"):
        (DST / split / "images").mkdir(parents=True)
        (DST / split / "labels").mkdir(parents=True)

    # Collect and shuffle image stems
    stems = sorted(p.stem for p in (SRC / "images").glob("*.jpg"))
    random.seed(SEED)
    random.shuffle(stems)

    val_count = int(len(stems) * VAL_RATIO)
    val_stems = set(stems[:val_count])

    # Copy and remap
    for stem in stems:
        split = "val" if stem in val_stems else "train"
        # Image
        shutil.copy2(
            SRC / "images" / f"{stem}.jpg",
            DST / split / "images" / f"{stem}.jpg",
        )
        # Label (remapped)
        label_src = SRC / "labels" / f"{stem}.txt"
        label_dst = DST / split / "labels" / f"{stem}.txt"
        remap_label(label_src, label_dst)

    # Find the new index for 一時停止 (class 130)
    ichiji_idx = CODE_TO_IDX[130]
    stop_line_idx = CODE_TO_IDX[136]

    # Generate data.yaml
    names_list = [CLASS_NAMES[code] for code in ORIGINAL_CODES]
    names_yaml = "\n".join(f"  {i}: {name}" for i, name in enumerate(names_list))

    yaml_content = f"""\
# GeoTechnologies Japan Road Signs — YOLOv8 dataset config
# Auto-generated by prepare_dataset.py

path: {DST.resolve()}
train: train/images
val: val/images

nc: {len(ORIGINAL_CODES)}
names:
{names_yaml}

# Key classes for BlueTicketDriving:
#   {ichiji_idx}: ichiji_teishi (一時停止)
#   {stop_line_idx}: stop_line (停止線)
"""
    (DST / "data.yaml").write_text(yaml_content)

    # Stats
    train_count = len(stems) - val_count
    print(f"Dataset prepared: {DST}")
    print(f"  Train: {train_count} images")
    print(f"  Val:   {val_count} images")
    print(f"  Classes: {len(ORIGINAL_CODES)}")
    print(f"  一時停止 (ichiji_teishi) → class index {ichiji_idx}")
    print(f"  停止線 (stop_line)       → class index {stop_line_idx}")


if __name__ == "__main__":
    main()
